

--[[ taken from Colton Ogden teaching the cs50 course 
     this function takes a sprite sheet and cuts it into a specified set
]]

function GenerateQuads(atlas, tilewidth, tileheight)
    -- total width of the sheet divided by how we want to split the tiles
    local sheetWidth = atlas:getWidth() / tilewidth
    -- total height of the sheet divided by how we want to split the tiles
    local sheetHeight = atlas:getHeight() / tileheight

    local sheetCounter = 1
    -- array to hold all of the sprites in
    local spritesheet = {}

    for y = 0, sheetHeight - 1 do
        for x = 0, sheetWidth - 1 do
            spritesheet[sheetCounter] =
                love.graphics.newQuad(x * tilewidth, y * tileheight, tilewidth,
                tileheight, atlas:getDimensions())
            sheetCounter = sheetCounter + 1
        end
    end

    -- return the total sprite sheet
    return spritesheet
end

